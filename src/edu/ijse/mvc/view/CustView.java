    private void loadTable(){
        try {
            String columns[]={"Cust. ID", "Cust. Title", "Cust. Name", "DOB", "Salary", "Cust. Address", "City", "Province", "Postal Code"};
            DefaultTableModel dtm = new DefaultTableModel(columns,0){
              
                @Override
                public boolean isCellEditable(int row, int column){
                  return false;
                }
            };
            jblCust.setModel(dtm);
            
             custDtos = custController.getAllCustDtos();
            for(CustDto dto : custDtos){
               Object[] rowData ={dto.getCustID(),dto.getCustTitle(),dto.getCustName(),dto.getDob(),dto.getSalary(),dto.getCustAddress(),
               dto.getCity(),dto.getProvince(),dto.getPostalCode()};
               dtm.addRow(rowData);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at loading Data to customer table");
        }
    }
