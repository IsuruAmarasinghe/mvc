    private void saveCust(){
        CustDto dto=new CustDto(jTextId.getText(),jTextCustTitle.getText(),jTextName.getText(),jTextDob.getText(),Double.parseDouble(jTextSalary.getText()),jTextAdrss.getText(),jTextCity.getText(),jTextProvince.getText(),jTextPCode.getText());
        String resp;
        try {
            resp = custController.saveCust(dto);
           // System.out.println(resp);
            JOptionPane.showMessageDialog(this, resp);
            clearForm();
        } catch (Exception ex) {
           // Logger.getLogger(CustView.class.getName()).log(Level.SEVERE, null, ex);
           JOptionPane.showMessageDialog(this, "Error at save data");
        }
        
     }
